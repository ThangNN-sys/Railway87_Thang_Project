
public class Program_Demo_001 {
// Hàm Main: Điểm bắt đầu để chạy chương trình
	public static void main(String[] args) {
// Máy tính hãy in ra cho tôi: .... -gõ tắt lấy gợi ý sout - Ctrl+SpaceBar
		System.out.println("Hello VIT");
		System.out.println("My name: Thang");
		System.out.println("My age: 30");
// Máy tính in ra tuổi của tôi là: ...
		// Khai báo biến cho lệnh in ra tuổi của tôi
		int age = 50;
		System.out.println("My age: " + age);
		// Khai báo biến cho lệnh in ra tên của tôi
		String name = "Nguyễn Ngọc Thắng";
		System.out.println("My name: " + name);
	}
}
